package com.sinosoftyingda.fastclaim.survey.page.three.model;

/**
 * 三者车
 * @author DengGuang
 */
public class SThreeCar {
	
	private boolean tIsEdited;		// 是否可编辑

	private String tID;				// 编号
	
	private String tPlate;			// 车牌号码
	
	private String tVin;			// 车架号
	
	private String tFactoryType;	// 厂牌类型
	
	private String tBlameNum;		// 责任比例
	
	private String tClaimNo;		// 报案号
	
	private String tCompany;		// 承保公司
	
	private String tEngine;			// 发动机号
	
	private String tPlateType;		// 号牌类型

	public boolean gettIsEdited() {
		return tIsEdited;
	}

	public void settIsEdited(boolean tIsEdited) {
		this.tIsEdited = tIsEdited;
	}

	public String gettID() {
		return tID;
	}

	public void settID(String tID) {
		this.tID = tID;
	}

	public String gettPlate() {
		return tPlate;
	}

	public void settPlate(String tPlate) {
		this.tPlate = tPlate;
	}

	public String gettVin() {
		return tVin;
	}

	public void settVin(String tVin) {
		this.tVin = tVin;
	}

	public String gettFactoryType() {
		return tFactoryType;
	}

	public void settFactoryType(String tFactoryType) {
		this.tFactoryType = tFactoryType;
	}

	public String gettBlameNum() {
		return tBlameNum;
	}

	public void settBlameNum(String tBlameNum) {
		this.tBlameNum = tBlameNum;
	}

	public String gettClaimNo() {
		return tClaimNo;
	}

	public void settClaimNo(String tClaimNo) {
		this.tClaimNo = tClaimNo;
	}

	public String gettCompany() {
		return tCompany;
	}

	public void settCompany(String tCompany) {
		this.tCompany = tCompany;
	}

	public String gettEngine() {
		return tEngine;
	}

	public void settEngine(String tEngine) {
		this.tEngine = tEngine;
	}

	public String gettPlateType() {
		return tPlateType;
	}

	public void settPlateType(String tPlateType) {
		this.tPlateType = tPlateType;
	}
}
