package com.sinosoftyingda.fastclaim.survey.page.three.model;

public class HistoryClaimBean {

	private String claimNo;
	private String goTime;//出险时间
	private String reportTime;//报案时间
	private String goAddress;//出险地点
	private String claimState;//赔案状态
	private String claimPrice;//赔案金额
	
	

	public String getClaimNo() {
		return claimNo;
	}
	public void setClaimNo(String claimNo) {
		this.claimNo = claimNo;
	}
	public String getGoTime() {
		return goTime;
	}
	public void setGoTime(String goTime) {
		this.goTime = goTime;
	}
	public String getReportTime() {
		return reportTime;
	}
	public void setReportTime(String reportTime) {
		this.reportTime = reportTime;
	}
	public String getGoAddress() {
		return goAddress;
	}
	public void setGoAddress(String goAddress) {
		this.goAddress = goAddress;
	}
	public String getClaimState() {
		return claimState;
	}
	public void setClaimState(String claimState) {
		this.claimState = claimState;
	}
	public String getClaimPrice() {
		return claimPrice;
	}
	public void setClaimPrice(String claimPrice) {
		this.claimPrice = claimPrice;
	}
	
	

}
